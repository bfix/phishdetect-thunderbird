function getDomainName(name) {
	var levels = name.split(".").reverse();
	var num = levels.length;
	var node = tldTrie;
	var match = "";
	var i;
	for (i = 0; i < num; i++) {
		var lvl = levels[i];
		var n = node.c[lvl];
		if (n === undefined) {
			match = lvl + "." + match;
			break;
		}
		if (match.length > 0) {
			match = "." + match;
		}
		match = lvl + match;
		node = n;
	}
	return match;
}
